numDiaSemana = input('Digite um número para saber o dia da semana correspondente: ')

if numDiaSemana == '1':
    print('Domingo')
elif numDiaSemana == '2':
    print('Segunda-feira')
elif numDiaSemana == '3':
    print('Terça-feira')
elif numDiaSemana == '4':
    print('Quarta-feira')
elif numDiaSemana == '5':
    print('Quinta-feira')
elif numDiaSemana == '6':
    print('Sexta-feira')
elif numDiaSemana == '7':
    print('Sábado')
else:
    print('Valor inválido!')